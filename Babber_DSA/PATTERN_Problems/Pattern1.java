package Babber_DSA.PATTERN_Problems;

public class Pattern1 {
    public static void main(String[] args) {
        int a = 3;
        for (int i = 1; i <= a; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.print("*");             
            }
            System.out.println();
        }
    }
}
